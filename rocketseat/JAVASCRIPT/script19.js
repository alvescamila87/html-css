/*  - Operadores de comparação:
        - == igual a 
        - != diferente de
        - === estritamente igual a 
        - !== estritamente diferente de 

*/

let one = 1
let two = 2

console.log(two == 1)
console.log(one == '1')

console.log(one != two)
console.log(one != 1)
console.log(one != '1')

console.log(one === 1) // number
console.log(one === '1') // string


console.log(two !== 2) // number
console.log(two !== '2') // string
